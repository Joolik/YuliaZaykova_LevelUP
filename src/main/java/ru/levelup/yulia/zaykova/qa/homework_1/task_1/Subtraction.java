/**
 * Вычитание чисел
 * int int
 * long long
 * double double
 */
public class Subtraction {

    public double subtract(int x, int y) {
        return (double) ( x - y );
    }

    public double subtract(long x, long y) {
        return (double) ( x - y );
    }

    public double subtract(double x, double y) {
        return  x - y;
    }
}
